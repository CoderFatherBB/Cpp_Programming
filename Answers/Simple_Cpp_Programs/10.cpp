// C++ Program To Display Size Of Different Data Type
#include<iostream>
using namespace std;
int main()
{
    cout << "sizeof(char)\t\t";
    cout << sizeof(char) << endl;
    cout << "sizeof(signed char)\t";
    cout << sizeof(signed char) << endl;
    cout << "sizeof(unsigned char)\t";
    cout << sizeof(unsigned char) << endl;
    cout << "sizeof(int)\t\t";
    cout << sizeof(int) << endl;
    cout << "sizeof(signed int)\t";
    cout << sizeof(signed int) << endl;
    cout << "sizeof(unsigned int)\t";
    cout << sizeof(unsigned int) << endl;
    cout << "sizeof(short)\t\t";
    cout << sizeof(short) << endl;
    cout << "sizeof(signed short)\t";
    cout << sizeof(signed short) << endl;
    cout << "sizeof(unsigned short)\t";
    cout << sizeof(unsigned short) << endl;
    cout << "sizeof(short int)\t";
    cout << sizeof(short int) << endl;
    cout << "sizeof(float)\t\t";
    cout << sizeof(float) << endl;
    cout << "sizeof(long)\t\t";
    cout << sizeof(long) << endl;
    cout << "sizeof(signed long)\t";
    cout << sizeof(signed long) << endl;
    cout << "sizeof(unsigned long)\t";
    cout << sizeof(unsigned long) << endl;
    cout << "sizeof(double)\t\t";
    cout << sizeof(double) << endl;
    cout << "sizeof(long double)\t";
    cout << sizeof(long double) << endl;
}